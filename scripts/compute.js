    const comboKey = `${svetaSign.name}-${guestSign.name}`;
    const reverseKey = `${guestSign.name}-${svetaSign.name}`;
    const special = specialCombos[comboKey] || specialCombos[reverseKey];
    const seed = makeSeed(svetaSign.name, guestSign.name, guestName, guestDate, index.toString());

    let realPart;
    let humorPart;

    if (special) {
        realPart = selectVariant(special.real, seed, special.real[0]);
        humorPart = selectVariant(special.humor, seed + 23, special.humor[0]);
    } else {
        const elementNote = getElementInsight(svetaSign, guestSign, seed);
        realPart = elementNote.real;
        humorPart = elementNote.humor;
    }

    if (guestSign.name === '–°–∫–æ—Ä–ø–∏–æ–Ω' && guestName && guestName.toLowerCase().includes('–º–∞—Ä–∏–Ω')) {
        humorPart = '–ò—Ö –¥—É—à–∏ —Å–æ–∑–¥–∞–Ω—ã, —á—Ç–æ–±—ã —Å–º–æ—Ç—Ä–µ—Ç—å —Ä–∏–ª—Å–∏–∫–∏ –∏ —á–∏—Ç–∞—Ç—å —à–∞–∫–∞–ª—å–Ω—ã–µ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–∏ –ø–æ–¥ –Ω–∏–º–∏.';
    }

