function compareLifePath(lifePath1, lifePath2) {
    const compatibilityMatrix = {
        1: [1, 5, 7],
        2: [2, 4, 6, 8],
        3: [3, 6, 9],
        4: [2, 4, 8],
        5: [1, 5, 7],
        6: [2, 3, 6, 9],
        7: [1, 5, 7],
        8: [2, 4, 6, 8],
        9: [3, 6, 9],
        11: [2, 11, 22],
        22: [4, 11, 22],
        33: [6, 9, 33]
    };
    
    const compatible = compatibilityMatrix[lifePath1] || [];
    if (compatible.includes(lifePath2)) return 85;
    if (lifePath1 === lifePath2) return 75;
    return 50;
}

