    const cleanHumor = lowerFirst(trimEnding(humorPart));

    const insight = `${svetaSign.name} (${svetaProfile.vibe}) –∏ ${guestSign.name} (${guestProfile.vibe}) ‚Äî ${cleanReal}, –∞ –µ—Å–ª–∏ —á–µ—Å—Ç–Ω–æ, ${cleanHumor}.`;

